    <article id="contraseña" style="background-color: rgb(179, 248, 225);">
        <h2>Olvide mi contraseña</h2>
        <ul>
            <p><b>Si estás registrado con otra cuenta de mail:</b></p>
            <li>Seleccioná Ingresar con mail y contraseña.</li>
            <li>Luego ingresá tu email y seleccioná Olvidé mi contraseña.</li>
            <li>Podrás elegir una nueva contraseña y seleccionar Registrar una nueva contraseña.</li>
            <li>Recibirás por mail el código de acceso que deberás ingresar y seleccionar Cambiar contraseña.</li>
        </ul>   
