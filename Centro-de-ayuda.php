    <article id="datos" style="background-color: rgb(172, 248, 223);">
        <h2>Como actualizo mis datos?</h2>
        <p>Ingresá a Mi cuenta / PERFIL. Una vez que te hayas registrado podés acceder a “Mis datos personales” donde podrás ver
        los datos que se brindaron al comienzo del registro. 
        <br>
        
        Desde aquí podrás modificar y continuar completando los datos de cualquiera de los campos que se muestran. Una vez
        completados se hace clic en el botón “Guardar cambios” para que los datos del perfil estén siempre actualizados.</p>

    </article>
    <br>
