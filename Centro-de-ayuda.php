    <article id="registro" style="background-color: rgb(178, 250, 226);">
        <h2> Como me registro?</h2>
        <ol>
            <li>Ingresá en Iniciar Sesión desde el menú desplegable que se encuentra en la parte superior izquierda o desde el icono de
            perfil, que se encuentra al lado del icono del carro, tenes distintas opciones.</li>
            <li>Para ingresar con tu cuenta de Gmail seleccioná Ingresar con Google, para ingresar con Facebook hacé click en el boton
            correspondiente y completa los datos de acceso a tu cuenta</li>
            <li>También podés hacerlo recibiendo clave de acceso rápido, para esto deberás hacer click en el botón correspondiente,
            ingresar tu mail en la ventana emergente y posteriormente cargar el codigo de acceso que se te enviará al mail que
            registraste, realizada esta acción podrás seguir ingresando tus datos en tu cuenta.</li>
            <li>En caso de que quieras registrarte con una cuenta de mail y contraseña, hace click en botón correspondiente, Ingresá tu
            mail y seleccioná "Regístrala ahora". Elegí tu contraseña y, a continuación confirma esta contraseña en el siguiente
            campo de formulario finalmente seleccioná Registrar una nueva contraseña.Recibirás por mail el código de acceso que
            deberás ingresar y seleccionar Cambiar contraseñ</li>
        </ol>

    </article>
